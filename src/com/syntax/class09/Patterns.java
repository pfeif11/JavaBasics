package com.syntax.class09;

public class Patterns {
	public static void main(String[] args) {
		for (int a = 1; a <= 5; a++) {
			for (int b = 5; b >= 1; b--) {
				System.out.print(b);	
			}
			System.out.println();
		}
		System.out.println("---------------------------");
		for(int c=5;c>=1;c--) {
			for(int d=5;d>=1;d--) {
				System.out.print(c);
			}
			System.out.println();
		}
	
	}

}
